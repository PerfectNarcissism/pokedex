package com.springboot.backend.apirest.entity;

public class Child {

	String name;
	String url;
	
	public Child() {
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}	
	
}
